#include "db_config.h"

#include "db_int.h"

/*
 * PUBLIC: u_int32_t __db_log2 __P((u_int32_t));
 */
u_int32_t
__db_log2(num)
	u_int32_t num;
{
	u_int32_t i, limit;

	limit = 1;
	for (i = 0; limit < num; limit = limit << 1)
		++i;
	return (i);
}
